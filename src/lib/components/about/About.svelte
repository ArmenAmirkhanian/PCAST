    <h2 class="text-xl font-bold mb-2">About PCAST</h2>
    <p class="text-gray-700 leading-relaxed">
      The <strong>Pavement Cracking Analysis &amp; Support Tool (PCAST)</strong> is a web-based
      decision-support application for concrete pavement engineers. It integrates cement hydration
      modeling, concrete maturity calculations, environmental loading, and early-age stress analysis
      to assist practitioners in planning saw-cut timing and evaluating early cracking risk in
      jointed plain concrete pavements (JPCP).
    </p>
